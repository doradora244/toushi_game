def normalize(text):
    """
    空白を全て取り除いて、採点用に文字列を正規化します。

    【なぜ正規化するのか？】
    「money = 100000」と「money=100000」は
    どちらも正しい Python ですが、文字列としては異なります。
    空白を除去してから比較することで、どちらも正解にできます。

    例:
        normalize("money = 100000") → "money=100000"
        normalize("  if  bug_rate > 20 : ") → "ifbug_rate>20:"
    """
    return text.strip().replace(" ", "")


def get_challenge(index):
    """
    指定したインデックスの問題を返します。
    存在しない場合は None を返します。

    引数:
        index (int): 問題番号（0 始まり）
    戻り値:
        dict | None
    """
    if 0 <= index < len(CHALLENGES):
        return CHALLENGES[index]
    return None


def check_answer(challenge, user_answer):
    """
    ユーザーの回答を採点します。

    【採点方法】
    1. ユーザーの入力を normalize() で正規化
    2. challenge["answers"] の各正解も normalize() で正規化
    3. どれか1つと一致すれば正解

    引数:
        challenge  (dict): 問題の辞書
        user_answer (str): ユーザーが入力した文字列
    戻り値:
        bool: True（正解）/ False（不正解）
    """
    # any() は「リストの中に True が1つでもあれば True を返す」関数
    # ジェネレータ式 (式 for 変数 in リスト) でリスト内包表記を遅延評価できる
    cleaned = normalize(user_answer)
    return any(normalize(a) == cleaned for a in challenge["answers"])


def get_total_challenges():
    """チャレンジの総数を返します。"""
    return len(CHALLENGES)


def get_display_answer(challenge):
    """
    不正解時に表示する「模範解答」を返します。
    answers リストの最初の要素を使います。
    """
    return challenge["answers"][0]
